package edu.dke.project12.physicsengine;

public class Vector2d {

    private double x;
    private double y;

    public Vector2d(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double get_x() {
        return x;
    }

    public double get_y() {
        return y;
    }

}
